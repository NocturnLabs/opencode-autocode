fn format_opencode_json(config: &Config) -> String {
    let sequential_thinking_enabled = config.mcp.use_sequential_thinking;
    let chrome_devtools_enabled = config
        .mcp
        .required_tools
        .iter()
        .any(|t| t.eq_ignore_ascii_case("chrome-devtools"));

    format!(
        r#"{{
  "$schema": "https://opencode.ai/config.json",
  "instructions": [
    ".forger/config.toml",
    ".forger/app_spec.md"
  ],
  "mcp": {{
    "sequential-thinking": {{
      "type": "local",
      "command": ["npx", "-y", "@modelcontextprotocol/server-sequential-thinking"],
      "enabled": {}
    }},
    "chrome-devtools": {{
      "type": "local",
      "command": ["npx", "-y", "chrome-devtools-mcp@latest"],
      "enabled": {}
    }}
  }},
  "permission": {{
    "*": "allow",
    "bash": "allow",
    "edit": "allow"
  }}
}}
"#,
        sequential_thinking_enabled, chrome_devtools_enabled,
    )
}
